// List Methods in Dart

void main() {
 // creating list
List a = [1, 2, 3, 4, 5];
print(a);
print('');


// adding single value in list
print('add single value in list');
a.add(6);
print(a);
print('');


// adding multiple values in list
print('add multiple values in list');
a.addAll([7, 8, 9]);
print(a);
print('');


//inserting value in list
print('insert value in list');
a.insert(0,0 );
print(a);
print('');


}